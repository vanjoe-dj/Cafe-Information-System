    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        try {
            Product product = new Product();
            product.setName(jTextField2.getText().trim());
            product.setPrice(Double.parseDouble(jTextField1.getText().trim()));

            // ---- SAVE IMAGE ----
            if (file != null) {
                product.setImage(Files.readAllBytes(file.toPath()));
            } else {
                product.setImage(null); // optional image
            }

            // ---- INSERT INTO DATABASE ----
            if (se.insertProduct(product)) {
                JOptionPane.showMessageDialog(null, "Product added successfully.");
                clear();
            } else {
                JOptionPane.showMessageDialog(null, "Failed!!", "Warning", 2);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid price format!", "Warning", 2);

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error reading image file!", "Warning", 2);

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Warning", 2);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

